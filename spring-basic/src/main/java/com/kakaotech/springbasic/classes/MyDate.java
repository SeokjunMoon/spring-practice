package com.kakaotech.springbasic.classes;

public class MyDate {
    private int year;
    private int month;
    private int date;
    private char day;


    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public int getMonth() {
        return month;
    }

    public void setMonth(int month) {
        this.month = month;
    }

    public int getDate() {
        return date;
    }

    public void setDate(int date) {
        this.date = date;
    }

    public char getDay() {
        return day;
    }

    public void setDay(char day) {
        this.day = day;
    }
}
